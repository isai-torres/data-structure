package Arrays;

public class ArrayExample {
  public static void main(String[] args) {
    // Multidimensional array
    int[][] matrix = {
      {2, 3, 4},
      {5, 6, 2},
      {7, 8, 9}
    };

    System.out.println(matrix[2][1]); // The first [2] is the row, the second [1] is the column
  }
}
